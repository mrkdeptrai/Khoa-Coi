    private void loadAllSupplier() {
        ArrayList<SupplierDBO> supplier = (ArrayList<SupplierDBO>) port.getAllSupplier();
        DefaultListModel<SupplierDBO> defaultModel = new DefaultListModel<>();

        for (SupplierDBO sup : supplier) {
            defaultModel.addElement(sup);
        }

        listSupplier.setModel(defaultModel);
    }
    
    public boolean checkInputValue() {
        String supplierID = txtSupplierID.getText();
        if (!getValidNumber(supplierID)) {
            JOptionPane.showMessageDialog(rootPane, " supplierID is not null value and must be numeric!)");
            return false;
        }
        try {
            String supplierName = txtSupplierName.getText();
            if (getValidString(supplierName) == true) {
            } else {
                JOptionPane.showMessageDialog(rootPane, " Suppplier name is not null value & Author name isn't containts numbers. Please input name of supplier correctly");
                return false;
            }
        } catch (InputMismatchException ea) {
                JOptionPane.showMessageDialog(rootPane, " Suppplier name isn't containts numbers. Please input name of supplier correctly");
        }
        String supplierPhone = txtSupplierPhone.getText();
        // Tạo đối tượng Pattern
        Pattern pattern = Pattern.compile(supplierPhone);
        // Lấy ra đối tượng Matcher
        Matcher matcher = pattern.matcher("\\d{9,11}");
        boolean checkPhone = matcher.matches();
        if (checkPhone) {
            JOptionPane.showMessageDialog(rootPane, " Supplier Phone is not null value and must be numeric (9 to 11 numbers)");
            return false;
        }
        return true;
    }

    public static boolean getValidNumber(String str1) {
        return str1.matches("\\d+");
    }

    public static boolean getValidString(String str2) {
        return str2.matches("\\D+");
    }
