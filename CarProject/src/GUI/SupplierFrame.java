    private void searchBySupplierID() {
        String supplierID = txtSearch.getText();
        if(!getValidNumber(supplierID)){
            loadAllSupplier();
        }
        else {
            ArrayList<SupplierDBO> supplier = (ArrayList<SupplierDBO>) port.getSupplierByID(supplierID);
            DefaultListModel<SupplierDBO> defaultModel = new DefaultListModel<>();

            for (SupplierDBO sup : supplier) {
                defaultModel.addElement(sup);
            }

            listSupplier.setModel(defaultModel);
        }
    }

    private void searchBySupplierName() {
        String supplierName = txtSearch.getText();
        if (supplierName.equals("")) {
            loadAllSupplier();
        } else {
            ArrayList<SupplierDBO> supplier = (ArrayList<SupplierDBO>) port.getSupplierByName(supplierName);
            DefaultListModel<SupplierDBO> defaultModel = new DefaultListModel<>();
            
            for (SupplierDBO sup : supplier) {
                defaultModel.addElement(sup);
            }

            listSupplier.setModel(defaultModel);
        }
    }
