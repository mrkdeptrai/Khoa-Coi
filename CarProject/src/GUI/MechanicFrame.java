    private void loadAllMechanic() {
        ArrayList<MechanicDBO> mechanic = (ArrayList<MechanicDBO>) port.getAllMechanic();
        DefaultListModel<MechanicDBO> defaultModel = new DefaultListModel<>();

        for (MechanicDBO mec : mechanic) {
            defaultModel.addElement(mec);
        }

        listMechanic.setModel(defaultModel);
    }
    
    public boolean checkInputValue() {
        String mechanicID = txtMechanicID.getText();
        if (!getValidNumber(mechanicID)) {
            JOptionPane.showMessageDialog(rootPane, " mechanicID is not null value and must be numeric!)");
            return false;
        }
        try {
            String mechanicName = txtMechanicName.getText();
            if (getValidString(mechanicName) == true) {
            } else {
                JOptionPane.showMessageDialog(rootPane, " Mechanic name is not null value & Author name isn't containts numbers. Please input name of mechanic correctly");
                return false;
            }
        } catch (InputMismatchException ea) {
                JOptionPane.showMessageDialog(rootPane, " Mechanic name isn't containts numbers. Please input name of mechanic correctly");
        }
        String mechanicPhone = txtMechanicPhone.getText();
        // Tạo đối tượng Pattern
        Pattern pattern = Pattern.compile(mechanicPhone);
        // Lấy ra đối tượng Matcher
        Matcher matcher = pattern.matcher("\\d{9,11}");
        boolean checkPhone = matcher.matches();
        if (checkPhone) {
            JOptionPane.showMessageDialog(rootPane, " Mechanic Phone is not null value and must be numeric (9 to 11 numbers)");
            return false;
        }
        return true;
    }

    public static boolean getValidNumber(String str1) {
        return str1.matches("\\d+");
    }

    public static boolean getValidString(String str2) {
        return str2.matches("\\D+");
    }
