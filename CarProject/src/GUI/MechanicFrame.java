    private void searchByMechanicID() {
        String mechanicID = txtSearch.getText();
        if (!getValidNumber(mechanicID)) {
            loadAllMechanic();
        } else {
            ArrayList<MechanicDBO> mechanic = (ArrayList<MechanicDBO>) port.getMechanicByID(mechanicID);
            DefaultListModel<MechanicDBO> defaultModel = new DefaultListModel<>();

            for (MechanicDBO mec : mechanic) {
                defaultModel.addElement(mec);
            }

            listMechanic.setModel(defaultModel);
        }
    }

    private void searchByMechanicName() {
        String mechanicName = txtSearch.getText();
        if (mechanicName.equals("")) {
            loadAllMechanic();
        } else {
            ArrayList<MechanicDBO> mechanic = (ArrayList<MechanicDBO>) port.getMechanicByName(mechanicName);
            DefaultListModel<MechanicDBO> defaultModel = new DefaultListModel<>();
            
            for (MechanicDBO mec : mechanic) {
                defaultModel.addElement(mec);
            }

            listMechanic.setModel(defaultModel);
        }
    }
